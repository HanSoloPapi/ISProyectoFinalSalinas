package vista;
public class UsuarioSR {
    
    int idUsuarioSR;
    String usuarioSR;
    String passwordSR;
    
    public UsuarioSR(){
    }

    public int getIdUsuarioSR() {
        return idUsuarioSR;
    }

    public void setIdUsuarioSR(int idUsuarioSR) {
        this.idUsuarioSR = idUsuarioSR;
    }

    public String getUsuarioSR() {
        return usuarioSR;
    }

    public void setUsuarioSR(String usuarioSR) {
        this.usuarioSR = usuarioSR;
    }

    public String getPasswordSR() {
        return passwordSR;
    }

    public void setPasswordSR(String passwordSR) {
        this.passwordSR = passwordSR;
    }



}
